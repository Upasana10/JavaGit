package practise.cm;

public class Swap {

	public static void main(String[] args) {
		int x=50;
		int y=40;
		System.out.println("Before swap");
		System.out.println("value of x"+x);
		System.out.println("Before swap"+y);
		 x=x+y;
	     y=x+y;
		 y=x-y;
		 System.out.println("After swap");
			System.out.println("value of x"+x);
			System.out.println("Before swap"+y);
	}

}
